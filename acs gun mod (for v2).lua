        rawset(v, 'BulletDrop', 0)
        rawset(v, 'CanBreak', false)
        rawset(v, 'adsTime', 0)
        rawset(v, 'RainbowMode', true)
        rawset(v, 'canAim', true)
        rawset(v, 'ShootType', 3)
    end
end
